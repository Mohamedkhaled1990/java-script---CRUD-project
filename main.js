document.getElementById("btn").addEventListener("click", function () {
    console.log("clicked");
    var weight = document.getElementById("weight-input").value
    var height = document.getElementById("height-input").value

    var bmi = weight / (height * height)

    document.getElementById("bmi").innerHTML = bmi

    if (bmi < 18) {

        document.getElementById("category").innerHTML = "Thin"
    } else if (bmi < 24.9) {
        document.getElementById("category").innerHTML = "Perfect"
    } else if (bmi < 29.9) {
        document.getElementById("category").innerHTML = "Over weight"
    } else if (bmi < 34.9) {
        document.getElementById("category").innerHTML = "Obesity 1"

    } else if (bmi < 39.9) {
        document.getElementById("category").innerHTML = "Obesity 2"

    } else {
        document.getElementById("category").innerHTML = "Dangerous"

    }

})









































































































































































































































